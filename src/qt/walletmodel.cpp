int WalletModel::getStakeForCharityPercent()
{

	if (!wallet->fStakeForCharity || wallet->vStakeForCharity.empty())
		return 0;
	int nTotal = 0;
	for (unsigned int i = 0; i < wallet->vStakeForCharity.size(); i++)
		nTotal += wallet->vStakeForCharity[i].nPercent;
	return nTotal;
}

